//! SDF (Signed Distance Field) 字体纹理生成
//!
//! 将位图字形转换为 SDF 纹理，实现高清无锯齿的文字渲染。

/// SDF 生成配置
pub struct SdfConfig {
    /// 字体内边距 (像素)，用于 SDF 计算的安全边界
    pub padding: usize,
    /// 每个字形的位图大小 (padding*2 + glyph_size)
    pub glyph_size: usize,
    /// 图集中的列数
    pub columns: usize,
}

impl Default for SdfConfig {
    fn default() -> Self {
        SdfConfig { padding: 4, glyph_size: 32, columns: 16 }
    }
}

