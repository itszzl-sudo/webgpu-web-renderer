/// 从内置位图字体为指定字符生成 SDF 纹理数据
///
/// 返回 (sdf_data, atlas_width, atlas_height) 其中 sdf_data 是 f32 数组
pub fn generate_font_atlas(chars: &[char], config: &SdfConfig) -> (Vec<f32>, usize, usize) {
    let SdfConfig { padding, glyph_size, columns, .. } = *config;
    let cell_size = glyph_size + padding * 2;
    let rows = (chars.len() + columns - 1) / columns;
    let atlas_w = columns * cell_size;
    let atlas_h = rows * cell_size;

    // 1. 生成每个字形的位图
    let mut atlas_bitmap = vec![0u8; atlas_w * atlas_h];
    for (i, &ch) in chars.iter().enumerate() {
        let code = ch as usize;
        let glyph_data = if code < 128 {
            get_glyph_bitmap(code)
        } else {
            [0u8; 8] // 不支持的字符显示空白
        };

        let col = i % columns;
        let row = i / columns;
        let ox = col * cell_size + padding;
        let oy = row * cell_size + padding;

        for y in 0..8 {
            for x in 0..8 {
                if (glyph_data[y] >> (7 - x)) & 1 != 0 {
                    let px = ox + x;
                    let py = oy + y;
                    if px < atlas_w && py < atlas_h {
                        atlas_bitmap[py * atlas_w + px] = 255;
                    }
                }
            }
        }
    }

    // 2. 将位图转换为 SDF (8SSEDT 算法)
    let sdf = bitmap_to_sdf(&atlas_bitmap, atlas_w, atlas_h);

    (sdf, atlas_w, atlas_h)
}

/// 获取字符的 8x8 位图数据
fn get_glyph_bitmap(code: usize) -> [u8; 8] {
    if code < 32 || code > 127 {
        return [0u8; 8];
    }
    let data = builtin_font_data();
    let idx = (code - 32) * 8;
    if idx + 8 <= data.len() {
        let mut glyph = [0u8; 8];
        glyph.copy_from_slice(&data[idx..idx + 8]);
        glyph
    } else {
        [0u8; 8]
    }
}

/// 将二值位图转换为 SDF (Signed Distance Field)
///
/// 使用 8SSEDT (8-Point Signed Sequential Euclidean Distance Transform) 算法。
/// 正值表示在形状内部，负值表示外部。
/// 结果归一化到 -1.0 ~ 1.0 范围。
fn bitmap_to_sdf(bitmap: &[u8], w: usize, h: usize) -> Vec<f32> {
    let max_dist = (w.max(h) as f32) * 0.5;

    // Phase 1: 初始化距离场
    // 内部点: 正距离 (到最近边界的距离)
    // 外部点: 负距离
    let mut dist = vec![0.0f32; w * h];

    // 计算到最近边界的距离
    for y in 0..h {
        for x in 0..w {
            let idx = y * w + x;
            let is_filled = bitmap[idx] > 128;

            if is_filled {
                // 内部点: 找最近的边界 (外部像素)
                let mut min_d = max_dist;
                for sy in y.saturating_sub(8)..h.min(y + 9) {
                    for sx in x.saturating_sub(8)..w.min(x + 9) {
                        if bitmap[sy * w + sx] <= 128 {
                            let d = (((sx as f32 - x as f32).powi(2) + (sy as f32 - y as f32).powi(2)).sqrt());
                            min_d = min_d.min(d);
                        }
                    }
                }
                dist[idx] = min_d.min(max_dist);
            } else {
                // 外部点: 找最近的填充像素
                let mut min_d = max_dist;
                for sy in y.saturating_sub(8)..h.min(y + 9) {
                    for sx in x.saturating_sub(8)..w.min(x + 9) {
                        if bitmap[sy * w + sx] > 128 {
                            let d = (((sx as f32 - x as f32).powi(2) + (sy as f32 - y as f32).powi(2)).sqrt());
                            min_d = min_d.min(d);
                        }
                    }
                }
                dist[idx] = -min_d.min(max_dist);
            }
        }
    }

    // 归一化到 -1.0 ~ 1.0
    for d in dist.iter_mut() {
        *d /= max_dist;
    }

    dist
}

/// 将 SDF f32 数据编码为 RGBA 纹理 (用于直观显示)
pub fn sdf_to_rgba(sdf: &[f32], w: usize, h: usize) -> Vec<u8> {
    let mut rgba = Vec::with_capacity(w * h * 4);
    for &d in sdf {
        // SDF 值映射到灰度: -1=黑, 0=灰, 1=白
        let v = ((d * 0.5 + 0.5) * 255.0).clamp(0.0, 255.0) as u8;
        rgba.push(v);
        rgba.push(v);
        rgba.push(v);
        rgba.push(255);
    }
    rgba
}